package pet.model.dto;

public class HospitalDTO {

	private int no;
	private String state;
	private String location_adr;
	private String road_adr;
	private String zip_code;
	private String name;
	private String coordinate_x;
	private String coordinate_y;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getLocation_adr() {
		return location_adr;
	}
	public void setLocation_adr(String location_adr) {
		this.location_adr = location_adr;
	}
	public String getRoad_adr() {
		return road_adr;
	}
	public void setRoad_adr(String road_adr) {
		this.road_adr = road_adr;
	}
	public String getZip_code() {
		return zip_code;
	}
	public void setZip_code(String zip_code) {
		this.zip_code = zip_code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCoordinate_x() {
		return coordinate_x;
	}
	public void setCoordinate_x(String coordinate_x) {
		this.coordinate_x = coordinate_x;
	}
	public String getCoordinate_y() {
		return coordinate_y;
	}
	public void setCoordinate_y(String coordinate_y) {
		this.coordinate_y = coordinate_y;
	}
	
	
}
